const SVG_W = 1114;
const SVG_H = 154;

// Seeded random for consistent SSR/client renders
function seededRandom(seed: number) {
  let s = seed;
  return () => {
    s = (s * 16807 + 0) % 2147483647;
    return (s - 1) / 2147483646;
  };
}

export default function GlitchLogo({
  color = "#DD04A8",
  inView,
}: {
  color?: string;
  inView?: boolean;
}) {
  const [started, setStarted] = useState(false);

  // Generate per-pixel timing (stable across renders)
  const pixelData = useMemo(() => {
    const rng = seededRandom(42);
    return PIXELS.map(() => {
      const rand = rng();
      return {
        revealDelay: rand * 0.6,
        offsetY: (rng() - 0.5) * 8,
      };
    });
  }, []);

  useEffect(() => {
    // If inView is controlled externally, use that
    if (inView !== undefined) {
      if (inView) {
        const t = setTimeout(() => setStarted(true), 200);
        return () => clearTimeout(t);
      }
      return;
    }
    // Default: start on mount
    const t = setTimeout(() => setStarted(true), 200);
    return () => clearTimeout(t);
  }, [inView]);

  return (
    <div style={{ width: "100%", position: "relative" }}>
      <svg
        viewBox={`0 0 ${SVG_W} ${SVG_H}`}
        width="100%"
        height="auto"
        fill="none"
        xmlns="http://www.w3.org/2000/svg"
        style={{ display: "block" }}
      >
        {PIXELS.map((px, i) => {
          const d = pixelData[i];

          return (
            <motion.rect
              key={i}
              x={px.x}
              y={px.y}
              width={px.w}
              height={px.h}
              fill={color}
              initial={{ opacity: 0, translateY: d.offsetY }}
              animate={
                started
                  ? { opacity: 1, translateY: 0 }
                  : { opacity: 0, translateY: d.offsetY }
              }
              transition={{
                opacity: {
                  duration: 0.01,
                  delay: d.revealDelay,
                },
                translateY: {
                  duration: 0.5,
                  ease: [0.625, 0.05, 0, 1],
                  delay: d.revealDelay,
                },
              }}
            />
          );
        })}
      </svg>
    </div>
  );
}
